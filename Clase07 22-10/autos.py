#Ordena alfabeticamente/numericamente permanentemente
autos = ['peugeot', 'chevrolet', 'audio', 'fiat', 'honda']
print(autos)
autos.sort()
print(autos)

#Ordena descendente
autos.sort(reverse=True)
print(autos)
#Ordena invertido
autos.reverse()
print(autos)
#Ordena temporalmente 
print(sorted(autos))